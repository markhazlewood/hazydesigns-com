						<h3>Prototype</h3>
						<p>
							The device prototype consists of a frame to mount a mobile phone equipped with augmented
							reality software (Layar) and a back-facing video camera.  An important design consideration
							was to avoid having the children obscure the camera with their fingers or hands while holding
							the device. This meant the handles needed to be sufficiently far from the camera, as well as
							provide a grip suitable for a child's small hands.  I was pleased with the results.
						</p>

