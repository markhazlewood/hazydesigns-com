						<h2>Primary Project - Candy Tracker</h2>
						<p>
							Candy Tracker is a mobile augmented reality experience for family tracking and
							social events.  It consists of a portable augmented reality device for use by
							children (during Halloween or other neighborhood event) and GPS tracking software
							for parents.  Basically the idea is that children can use the mobile device to
							track down candy in their neighborhood on Halloween and parents can stay home and
							track their kids' whereabouts.
						</p>

						<h3>My Contribution</h3>
						<p>
							For this project, I <strong>designed and built the mobile device prototype</strong>.
							I <strong>implemented the augmented reality prototype software using the Layar API</strong>
							and entered the POIs and icons into my own hosted database.  I <strong>planned and
							executed the trial usability tests</strong> with two children (the parent tests were
							done by another member of the group).  Finally, I <strong>designed both the "sales pitch"
							poster as well as the much more detailed "academic" poster</strong>.
						</p>

